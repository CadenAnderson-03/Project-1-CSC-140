#include <iostream>

int main() {
    
    // Declaring my two integer variables
    int num1 = 15;

    int num2 = 2;

    // The arithmetic operations to do with the two integers
    std::cout << num1 << " + " << num2 << " = " << (num1 + num2) << std::endl;

    std::cout << num1 << " - " << num2 << " = " << (num1 - num2) << std::endl;

    std::cout << num1 << " * " << num2 << " = " << (num1 * num2) << std::endl;

    std::cout << num1 << " / " << num2 << " = " << (num1 / num2) << std::endl;

    std::cout << num1 << " % " << num2 << " = " << (num1 % num2) << std::endl;

    return 0;
}